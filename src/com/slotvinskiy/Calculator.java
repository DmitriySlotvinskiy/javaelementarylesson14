package com.slotvinskiy;

public class Calculator {

    public double add(int x, int y) {
        return x + y;
    }

    public double subtract(int x, int y) {
        return x - y;
    }

    public double multiply(int x, int y) {
        return (double) x * y;
    }

    public double divide(int x, int y) {
        return (double) x / y;
    }
}
